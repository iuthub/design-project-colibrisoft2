function validate(form) {
  var re = /^[a-z,A-Z,а-я,А-Я]+$/i;
  var ph = /^[+,0-9]+$/i;
  var ph2 = /^[0-9]+$/i;
  
  if (!re.test(form.LName.value)||!re.test(form.FName.value)) {
    alert('Введите только буквы от a до z или а до я');
